// Here is a simple example of creating a main.cpp file 
// for a basic C++ program:

// ```cpp
#include <iostream>  // Include input/output stream library

int main() {
    std::cout << "Hello, World!" << std::endl;  // Print Hello World to console
    return 0;  // Exit the program with success status
}
// ```

// This file includes the essential parts:
// - Preprocessor directive `#include <iostream>` for standard input/output.
// - `main()` function, the entry point of any C++ program.
// - Inside `main()`, a simple print statement outputs text to the console.
// - The program returns 0 to indicate successful execution.

// You can create this file in any text editor, save it as `main.cpp`, and compile it with a C++ compiler like `g++` or `clang++` using a command such as:

// ```
// g++ main.cpp -o main
// ```

// Then run the executable `main` to see the output.

